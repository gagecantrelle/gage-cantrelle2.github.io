function every(arr, test) {
  //create a varaible that will hold true or false
  var istrue = true
  //loop through arr
  for(let i = 0; i < arr.length; i++){
    //run the given test function
    if(!test(arr[i])){
      //set is true to false
      istrue = false;
    }
  }
//return istrue
return istrue;
}

